XYZ CentDiff(int x1, int y1, int z1)
{
	XYZ n;
	n.x = 0.5*(Data((x1 + 1) >= vol_dim[0] ? vol_dim[0] - 1 : x1 + 1, y1, z1) - Data((x1 - 1) < 0 ? 0 : x1 - 1, y1, z1));
	n.y = 0.5*(Data(x1, (y1 + 1) >= vol_dim[1] ? vol_dim[1] - 1 : y1 + 1, z1) - Data(x1, (y1 - 1) < 0 ? 0 : y1 - 1, z1));
	n.z = 0.5*(Data(x1, y1, (z1 + 1) >= vol_dim[2] ? vol_dim[2] - 1 : z1 + 1) - Data(x1, y1, (z1 - 1) < 0 ? 0 : z1 - 1));
	
	float Norm = sqrt(n.x * n.x + n.y * n.y + n.z * n.z);

	if (Norm > 0.){
		Norm = 1. / Norm;
		n.x *= Norm;
		n.y *= Norm;
		n.z *= Norm;
	}
	
	return n;
}

/*
Linearly interpolate the position where an isosurface cuts
an edge between two vertices, each with their own scalar value
*/
void VertexInterp(double isolevel, XYZ p1, XYZ p2, double valp1, double valp2, XYZ &p, XYZ &n)
{
	double mu;
	
	XYZ n1 = CentDiff(p1.x, p1.y, p1.z);
	XYZ n2 = CentDiff(p2.x, p2.y, p2.z);

	if (abs32(isolevel - valp1) < 0.00001)
	{
		p = p1;
		n = n1;
		return;
	}
		
	if (abs32(isolevel - valp2) < 0.00001)
	{
		p = p2;
		n = n2;
		return;
	}
		
	if (abs32(valp1 - valp2) < 0.00001)
	{
		p = p1;
		n = n1;
		return;
	}
		
	mu = (isolevel - valp1) / (valp2 - valp1);

	p.x = p1.x + mu * (p2.x - p1.x);
	p.y = p1.y + mu * (p2.y - p1.y);
	p.z = p1.z + mu * (p2.z - p1.z);

	n.x = n1.x + mu * (n2.x - n1.x);
	n.y = n1.y + mu * (n2.y - n1.y);
	n.z = n1.z + mu * (n2.z - n1.z);

	
	double Norm = sqrt(n.x * n.x + n.y * n.y + n.z * n.z);

	if (Norm > 0.){
		Norm = 1. / Norm;
		n.x *= Norm;
		n.y *= Norm;
		n.z *= Norm;
	}
	
	
}

XYZ TriangNorm(XYZ p1, XYZ p2, XYZ p3){
	XYZ n;
	double Norm = 0.;
	n.x = (p2.y - p1.y)*(p2.z - p3.z)
		- (p2.y - p3.y)*(p2.z - p1.z);
	n.y = (p2.z - p1.z)*(p2.x - p3.x)
		- (p2.z - p3.z)*(p2.x - p1.x);
	n.z = (p2.x - p1.x)*(p2.y - p3.y)
		- (p2.x - p3.x)*(p2.y - p1.y);

	
	Norm = sqrt(n.x * n.x + n.y * n.y + n.z * n.z);

	if (Norm > 0.){
		Norm = 1. / Norm;
		n.x *= Norm;
		n.y *= Norm;
		n.z *= Norm;
	}
	return n;
}



